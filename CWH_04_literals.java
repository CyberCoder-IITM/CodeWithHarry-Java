public class CWH_04_literals {
    public static void main(String[] args) {
        byte age = 34; // byte is of 1 byte
        System.out.println(age);
        int age1 = 54; // int is of 4 byte
        short age2 = 93; // short is of 2 byte
        float a = 2.2f; // float is of 4 byte
        double b = 4.2; // double is of 8 byte
        long c = 398947927429l; // long is of 8 byte
        char d = 'A'; // char is of 2 byte
        boolean e = true; // the size of boolean depends on jvm
        System.out.println(a);

    }
}
